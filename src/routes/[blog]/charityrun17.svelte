        <h1>Training</h1>
        <p>Eine Woche vor Startschuss krieg ich diese E-Mail: "Am 21.05. um 12Uhr Laufen. Begenen. Helfen". Der Inhalt
            zusammengefasst:
            <br>"Vielen Dank, dass sie letztes mal teilgenommen haben. Oh, by the way: Nächste Woche ist das Rennen für
            dieses
            Jahr".
            <br>Oh wow.</p>
        <p>Also alles Training was ich bis dato geleistet habe bestand aus Basistraining und 1-2 verteilte Qualitätsläufe
            in
            einigen Wochen. Ich sah dieses Rennen als Vorbereitung zu meinem eigentlichen Ziel an - dem Rheinuferlauf in 2
            Monaten. Das bietet mir die Möglichkeit an meinem Vorbereitungsrituatl für ein Rennen zu feilen und auch etwas
            mit dem Essen und Warm-Up vor dem Rennen zu experimentieren.</p>


        <h1>Vor dem Rennen</h1>
        <p>Dinge die ich bisher gelernt habe: Man kann nie früh genug da sein. Man kann nie früh genug zur Toilette
            gehen.
            <br> Dinge die ich lernen musste: Man KANN sich zu früh aufwärmen. Nämlich 5km-zu-lang.
            <img src="/006_charityrun17/warmup.png" alt="warmup">
        </p>

        <h1>Im Rennen</h1>
        <p>
            <strong>1. Kilometer:</strong> Mit dem Startschuss ging das Gedrängle los. 10km Läufer, 5km Läufer und 5km
            Walker alle
            gleichzeitig. 150 Teilnehmer. Noch so beiläufig wurde uns erwähnt, dass das Ausgangstor aus dem Park nicht
            geöffnet
            werden konnte. Also wer hinter den Walkern raus wollte hat einbußen müssen. Das hab ich mir nicht erlauben
            lassen.</p>
        <p>
            <strong>2. Kilometer:</strong> Die Strecke war nun gerade, das Gedrängle hat aufgehört. Komplett. Vorne war nur
            noch Sicht
            auf 10-20 und nach hinten wollte ich garnicht mehr blicken.</p>
        <p>
            <strong>3. Kilometer:</strong> Bei der Kurve in den Wald fing die Katastrophe an: Seitenstechen. Aber von der
            Art wie ich
            sie nur 1-2 Mal im Training erleben musste. Da hat auch der einfache Trick mit der Atmung auf dem anderen Bein
            nicht mehr geholfen hilft. Komplett crash</p>
