    <h1>Vorteile die gepriesen werden</h1>
    <ol>
        <li>Die Möglichkeit eine Einsicht über die Gefühlslage der Vergangenheit zu haben: Einem Tagebuch kann man besser vertrauen als den eigenen Erinnerungen: "War ich vor einem Jahr so traurig/glücklich wie ich es in Erinnerung hatte?"</li>
        <li>Externalisieren von Emotionen auf eine physische (oder eher: digitale) Seite ist eine Art diese zu Verarbeiten.</li>
        <li>Übung Gedanken kohärent zu organisieren.</li>
        <li>Pläne machen. Fortschritt verfolgen. Rechenschaft leisten.</li>
    </ol>

