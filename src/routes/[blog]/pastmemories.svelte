        <h1>Revue</h1>
        <p>Das Video hat so seine Höhepunkte und Tiefpunkte, leider. Die meiste Zeit war ich
            der Meinung die Idee und das Konzept sind zu übertrieben "tiefsinning" geworden,  <br />
            sodass sie gar schon albern wirken. Zudem passiert nicht viel auf dem Bildschirm <br />
            Es ist alles etwas langweiliger geworden als ich es mir vorgestellt habe. <br />
            Es wird immer anstrengender und anstrengender mit Teeworlds was zu machen. Viele Stunden
            editieren für wenige Sekunden was sich sehen lässt. </p>

        <h1>Zukunft</h1>
        <p>
            Mit ResamVi waren ursprünglich RuneScape Music Videos (kurz RSMV, daher auch der Name ReSaMVi) gedacht. Es sollte
            ein Bedürfniss kratzen was mich schon lange gejuckt hatte: Editierte Videos, untermahlt durch Musik selber erstellen.
        </p>
        <p>
            Teeworlds hat sich hier zufälligerweise als Canvas gut geeignet. Es ist überraschend und witzig was ich an Bekanntheit dadurch erlangen konnte.
        </p>
        <p>
            Für's erste war es das auch wieder mit Videos.
            Das hier soll nicht in "Ich bin ein Künstler"-Gerede enden. Ich sehe mich nur gerade mit Ideen am Limit was sich mit - nur zweidimensionalen -
            Teeworlds Demos noch machen lässt editier-technisch. Ich bin schließlich nur Informatikstudent und kein Filmdirektor, verdammt.
