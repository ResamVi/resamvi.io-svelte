        <h1>Renninformationen</h1>
        <p>Seit dem Marathon im März muss ich einsehen der 10k ist doch die Disziplin in der ich mich wohlfühle.</p>
        <p>Die Strecke ist zu lang um sie im Vollsprint zu absolvieren, heißt man fühlt sich nicht durchgängig gezwungen sein
            eigenes Leid zu vergrößern nur um danach ans Ziel zu gelangen - dabei jegliche Feinmotorik die ein Mensch im Laufe
            seines Lebens lernte in den Hintergrund geschoben - und mit der Anmut eines Steins zu Boden zu plumsen (ich rede
            hier von 5km Läufen).</p>
        <p>Trotzdem ist ein Maß an Geschwindigkeit erforderlich, wo selbst Rentner auf Fahrrädern nicht mehr hinterherkommen
            (habe ich in meinem Training herausgefunden).</p>
        <p>Wie schon erwähnt, der Schlossparklauf in Karlsruhe war der Vorbereiter. Der Zwischenschritt.</p>
        <p>Now's the real deal:</p>
        <p>Diesmal soll es auf's Podest.</p>
        <p>Wo: Kehl</p>
        <p>Wie weit: 10 km</p>
        <p>Webseite: <a href="https://www.kehler-rheinuferlauf.de/">https://www.kehler-rheinuferlauf.de/</a> </p>

        <img src="/009_rheinuferlauf17/strecke.jpg" alt="strecke">

