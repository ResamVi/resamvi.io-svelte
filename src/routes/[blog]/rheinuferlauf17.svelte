        <h1>Training</h1>
        <p>Hier hat sich immernoch keine Struktur gefunden. Einziger Trost war eigentlich, dass ich konsistent Läufe absolvierte.</p>
        <img src="/009_rheinuferlauf17/training.gif" alt="training">
        <p>Sehr schön erkennbar: Grün sind Trainingsläufe, Rot sind Wettläufe (Bienwaldmarathon, Schlossparklauf) nach rot kommt
            lange Zeit nichts, weil ich eigentlich immer verletzt danach war. Ansonsten rangieren die Zahlen zwischen 3 bis
            5 Läufe pro Woche.</p>

        <h1>Vor dem Rennen</h1>
        <p>Aus den Fehlern des Schlossparklaufs hab ich gelernt. Mein Warm-Up war nichts abartig anstrengendes. Bei den anderen
            habe ich schon Sachen gesehen, das wäre bei mir schon "kaputt machen".

        <h1>Im Rennen</h1>
        <p>
            <strong>1. Kilometer:</strong> Ehrlich zugegeben der Start war angenehm. Das lag wahrscheinlich an der niedrigen Teilnehmeranzahl
            von 67. Ich hab mich in meiner Position im Starthaufen nicht verschätzt. Im vorderen Drittel, leider nicht vorne
            genug um auf irgendwelchen Bildern zu sein. Man würde mich sowieso nur (tief in Gedanken) auf die Leute starren
            sehen.</p>

        <img src="/009_rheinuferlauf17/anfang.jpg" alt="konkurrenz">

        <p>
            <strong>2. Kilometer:</strong> Im 2. Kilometer ging es in dem Wald. Sehr ähnlich zu Karlsruhe. Aber man sieht schon aus
            dem obigen Bild das Wetter ist sehr trüb, es nieselt nämlich. Der Boden ist leicht matschig und man muss Pfützen
            ausweichen. Das sind Kleinigkeiten mit denen man sich abfindet dafür ist es ein PERFEKTES Wetter für einen optimalen
            Lauf.</p>
        <p>Wären nicht die - nennen wir sie doch: "Space Invaders". Ja, der Weg ist sehr schmal. Ja, es ist windig, dann läuft
            eben hinter mir für das bisschen Windschatten.</p>
        <p>Aber Neeeeein wir müssen nicht aus jeder Kurve einen Sexakt machen. Aber hey, hauptsache 20cm in der Kurve gespart.</p>

        <p>
            <strong>3. Kilometer:</strong> Kurve am Kulturwehr vorbei. Ab hier bin ich eigentlich schon sehr allein - nicht weil
            ich weit vorne oder weit hinten bin - eher weil allen Feuerwerkläufer das Pulver ausgegangen ist. Viel Treibladung
            am Anfang. Buntes Feuerwerk. tiefer Absturz danach. Hätte ja für sieben Runden um's Stadium gerreicht. Tja, sind
            halt noch weitere 18 wenn wir schon in Runden zählen.</p>

        <p>
            <strong>4. Kilometer:</strong> Lange geradeaus an der Rheinpromenade. Die Trinkstationen werden ignoriert. Ich pack das
            einfach nicht entweder huste ich wie ein Kettenraucher oder verschütte soweiso alles beim Laufen. Ab jetzt wurde
            es auch für mich unangenehm anstrengend. Seitenstechen haben angefangen aber immerhin nichts verheerendes diesmal.
            Mama habe ich auch gesehen, da läuft man dann wieder ein bisschen schneller :-)</p>

        <p>
            <strong>5. Kilometer:</strong> Erste Runde absolviert. Hier werden noch Fotos gemacht. Da war noch genug Lebensfreude
            in mir um zu lächeln. Heißt auch da wäre noch genug Energie um schneller zu laufen. Ich hab jetzt alles schon
            gesehen jetzt weiß ich worauf ich mich einlassen muss in der 2. Runde.</p>

        <p>
            <strong>6. Kilometer:</strong> So ziemlich alleine geht es für mich also weiter in die nächste Runde. Nochmal an den
            Wohnhäusern des Kronenhofs. Eigentlich echt witzig wie mich alle anlächeln und ich mich hier freiwillig quäle.
            Fast schon peotisch. Das waren ein Gedanke die mir so durch den Kopf ging während des Laufes. Da ist sowieso
            nicht viel Kapazität für anspruchsvollere Gedankengänge.</p>

        <p>
            <strong>7. Kilometer:</strong> Was cool ist jemand war Grenzposten den ich kenne, (Hey, T.) und hat ein bisschen was
            aufgenommen:</p>

        <video width="320" controls loop muted>
            <source src="/009_rheinuferlauf17/form.webm" type="video/webm" /> Your browser does not support the video tag.
        </video>

        <p>Meine Laufform ist vielleicht doch garnicht so schlecht. Sieht echt aus als ob ich langsam laufe aber ich war bei
            4:20 min/km oder 13.85 km/h unterwegs zu dem Zeitpunkt.</p>

        <p>
            <strong>8. und 9. Kilometer:</strong> Geht Richtung Endstation. Nicht viel zu sagen. Mir gehen gerade die Superlative
            aus. Ist aber schon hart gewesen.</p>

        <p>
            <strong>10. Kilometer:</strong> An den Endspurt ins Stadion. Mama nochmal gesehen. Meinen Namen haben sie nicht richtig
            ausgesprochen aber ganz ehrlich meine einzigen Prioritäten sind gerade ob ich kotzen werde und wenn ja wohin.
            (Soweit kam's zum Glück nicht).</p>

        <p>Ich hätte echt gern noch mehr Bilder gezeigt aber die kriegen es nicht hin alle Bilder hochzuladen. Schon 2 Monate
            warte ich hier.</p>
        <p>
            <strong>Ich hab's nämlich geschafft. Ich bin Zweiter!</strong>
        </p>

